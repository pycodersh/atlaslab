const existingPhrases = new Set()
for (const line of src.split('\n')) {
  const m = line.match(/phrase:\s*'([^'\\]*(?:\\.[^'\\]*)*)'/)
  if (m) existingPhrases.add(m[1].replace(/\\'/g, "'").toLowerCase())
}

