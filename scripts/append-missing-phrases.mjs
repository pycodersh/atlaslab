console.log(`Adding ${toAdd.length} new entries (${newEntries.length - toAdd.length} already exist)`)

// Format new entries - NOTE: use single quotes and escape apostrophes in phrase/meaning
const lines = toAdd.map(e => {
  const phrase = e.phrase.replace(/'/g, "\\'")
  const meaning = e.meaning.replace(/'/g, "\\'")
  const p = `'${phrase}'`.padEnd(46)
  const m = `'${meaning}'`.padEnd(38)
  return `  { phrase: ${p} meaning: ${m} type: '${e.type}' },`
}).join('\n')

// Find the closing ] of the phraseDict array (first standalone ] line after the array opening)
const lines2 = src.split('\n')
let insertLineIdx = -1
let inArray = false
for (let i = 0; i < lines2.length; i++) {
  if (lines2[i].includes('export const phraseDict')) { inArray = true; continue }
  if (inArray && lines2[i].trim() === ']') { insertLineIdx = i; break }
}

if (insertLineIdx === -1) {
  console.error('Could not find closing ] of phraseDict array')
  process.exit(1)
}

console.log(`Inserting before line ${insertLineIdx + 1}: "${lines2[insertLineIdx]}"`)

// Insert before that line
lines2.splice(insertLineIdx, 0,
  '  // ── Auto-generated: missing phrase entries ──────────────────────────────',
  lines
)

fs.writeFileSync('./data/patto-phrase-dictionary.ts', lines2.join('\n'))
console.log('Done!')
