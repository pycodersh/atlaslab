// ─── Lookup ───────────────────────────────────────────────────────────────────

const _index = new Map<string, PhraseDictEntry>()
for (const entry of phraseDict) {
  _index.set(entry.phrase.toLowerCase(), entry)
}

export function lookupPhraseMeaning(phrase: string): PhraseDictEntry | undefined {
  const lower = phrase.toLowerCase().trim()
  if (_index.has(lower)) return _index.get(lower)

  // Normalize common conjugations to base form
  const normalized = normalizePhrase(lower)
  if (normalized !== lower && _index.has(normalized)) return _index.get(normalized)

  return undefined
}

function normalizePhrase(phrase: string): string {
  // walked through → walk through, looked forward to → look forward to
  const words = phrase.split(' ')
  if (words.length < 2) return phrase

  const irregular: Record<string, string> = {
    went: 'go', came: 'come', took: 'take', made: 'make', gave: 'give',
    got: 'get', kept: 'keep', felt: 'felt', found: 'find', held: 'hold',
    put: 'put', set: 'set', cut: 'cut', ran: 'run', sat: 'sit',
    stood: 'stand', fell: 'fall', broke: 'break', built: 'build',
    brought: 'bring', bought: 'buy', caught: 'catch', led: 'lead',
    left: 'leave', lost: 'lose', meant: 'mean', sent: 'send',
    spent: 'spend', stuck: 'stick', told: 'tell', thought: 'think',
    wore: 'wear', won: 'win', wrote: 'write', heard: 'hear',
    saw: 'see', said: 'say', knew: 'know', grew: 'grow',
    drew: 'draw', flew: 'fly', lay: 'lie', paid: 'pay',
    rode: 'ride', rose: 'rise', drove: 'drive', chose: 'choose',
    woke: 'wake', spoke: 'speak', dug: 'dig', hung: 'hang',
    lit: 'light', swam: 'swim', sang: 'sing', rang: 'ring',
    began: 'begin', bore: 'bear', blew: 'blow', forgot: 'forget',
    forgave: 'forgive', hid: 'hide', hurt: 'hurt', overcame: 'overcome',
    slept: 'sleep', sped: 'speed', swept: 'sweep', swore: 'swear',
    threw: 'throw', tore: 'tear', understood: 'understand', withdrew: 'withdraw',
  }

  let first = words[0]
  // Past tense -ed
  if (first.endsWith('ed') && first.length > 4) {
    const base1 = first.slice(0, -2)
    const base2 = first.slice(0, -1)
    const candidate1 = [base1, ...words.slice(1)].join(' ')
    const candidate2 = [base2, ...words.slice(1)].join(' ')
    if (_index.has(candidate1)) return candidate1
    if (_index.has(candidate2)) return candidate2
  }
  // -ing
  if (first.endsWith('ing') && first.length > 5) {
    const base1 = first.slice(0, -3)
    const base2 = first.slice(0, -3) + 'e'
    const candidate1 = [base1, ...words.slice(1)].join(' ')
    const candidate2 = [base2, ...words.slice(1)].join(' ')
    if (_index.has(candidate1)) return candidate1
    if (_index.has(candidate2)) return candidate2
  }
  // Irregular
  if (irregular[first]) {
    const candidate = [irregular[first], ...words.slice(1)].join(' ')
    if (_index.has(candidate)) return candidate
  }

  return phrase
}
